    public void showData(){
        data.removeAllElements();
        for(Ban b : dsban){
            Vector v = new Vector();
            v.add(b.getMaban()); v.add(b.getTenban()); v.add(b.getTrangthai());
            data.add(v);
        }
        tbban.setModel(new DefaultTableModel(data, title));
    }
