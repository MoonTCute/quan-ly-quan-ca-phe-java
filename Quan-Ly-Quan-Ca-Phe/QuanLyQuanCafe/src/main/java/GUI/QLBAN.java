    private void tbbanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbbanMouseClicked
        // TODO add your handling code here:
        int row = tbban.getSelectedRow();
        String maban = tbban.getValueAt(row, 0).toString().trim();
        String tenban = tbban.getValueAt(row, 1).toString().trim();
        txtmaban.setText(maban);
        txttenban.setText(tenban);
        btluu.setEnabled(false);
        btxoa.setEnabled(true);
        btsua.setEnabled(true);
    }//GEN-LAST:event_tbbanMouseClicked

    private void btluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btluuActionPerformed
        // TODO add your handling code here:
        String maban;
        String tenban;
        String trangthai = "";
        maban = txtmaban.getText();
        tenban = txttenban.getText();
        Ban b = new Ban(maban,tenban,trangthai);
        BANDAO.UpdateSQL(b);
        dsban = BANDAO.getDSB();
        showData();
        txtmaban.setText("");
        txttenban.setText("");
        btsua.setEnabled(true);
        btxoa.setEnabled(true);
    }//GEN-LAST:event_btluuActionPerformed

    private void btxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btxoaActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá?","Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            String maban;
            String tenban;
            maban = txtmaban.getText();
            tenban = txttenban.getText();
            BANDAO.DeleteSQL(maban, tenban);
            data.removeAllElements();
            dsban = BANDAO.getDSB();         
            showData();
            txtmaban.setText("");
            txttenban.setText("");
        }
    }//GEN-LAST:event_btxoaActionPerformed

    private void btsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsuaActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa?","Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            String maban;
            String tenban;
            String trangthai ="";
            maban = txtmaban.getText();
            tenban = txttenban.getText();
            Ban b = new Ban(maban, tenban, trangthai);
            BANDAO.FixSQL(b);
            data.removeAllElements();
            dsban = BANDAO.getDSB();         
            showData();
            txtmaban.setText("");
            txttenban.setText("");
        }
    }//GEN-LAST:event_btsuaActionPerformed

