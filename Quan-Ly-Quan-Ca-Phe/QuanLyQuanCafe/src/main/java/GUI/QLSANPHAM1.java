    private void tbspMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbspMouseClicked
        // TODO add your handling code here:
        int row = tbsp.getSelectedRow();
        String masp = tbsp.getValueAt(row, 0).toString().trim();
        String tensp = tbsp.getValueAt(row, 1).toString().trim();
        String giatien = tbsp.getValueAt(row, 2).toString().trim();
        String dvt = tbsp.getValueAt(row, 3).toString().trim();
        String malsp = tbsp.getValueAt(row, 4).toString().trim();
        txtmasp.setText(masp);
        txttensp.setText(tensp);
        txtgiatien.setText(giatien);
        txtdonvitinh.setText(dvt);
        txtmalsp.setText(malsp);
        btluu.setEnabled(false);
        btxoa.setEnabled(true);
        btsua.setEnabled(true);
    }//GEN-LAST:event_tbspMouseClicked

