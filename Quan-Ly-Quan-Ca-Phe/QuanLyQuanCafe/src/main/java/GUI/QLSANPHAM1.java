    private void btxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btxoaActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá?","Thông báo",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION){
            String masp;
            masp = txtmasp.getText();
            SP1DAO.DeleteSQL(masp);
            data.removeAllElements();
            dssp = SP1DAO.getDSSP();
            showData();
            txtmasp.setText("");
            txttensp.setText("");
            txtdonvitinh.setText("");
            txtgiatien.setText("");
            txtmalsp.setText("");
        }
    }//GEN-LAST:event_btxoaActionPerformed

    private void btluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btluuActionPerformed
        // TODO add your handling code here:
        String masp;
        String tensp;
        int giatien;
        String dvt;
        String malsp;
        masp = txtmasp.getText();
        tensp = txttensp.getText();
        giatien = Integer.parseInt(txtgiatien.getText());
        dvt = txtdonvitinh.getText();
        malsp = txtmalsp.getText();
        Sanpham sp = new Sanpham(masp,tensp,giatien,dvt,malsp);
        SP1DAO.UpdateSQL(sp);
        data.removeAllElements();
        dssp = SP1DAO.getDSSP();
        showData();
        txtmasp.setText("");
        txttensp.setText("");
        txtdonvitinh.setText("");
        txtgiatien.setText("");
        txtmalsp.setText("");
    }//GEN-LAST:event_btluuActionPerformed

