    public void showData(){
        data.removeAllElements();
        for(Sanpham sp : dssp){
            Vector v = new Vector();
            v.add(sp.getMasp()); v.add(sp.getTensp()); v.add(sp.getGiatien()); v.add(sp.getDvt()); v.add(sp.getMalsp());
            data.add(v);
        }
        tbsp.setModel(new DefaultTableModel(data, title));
    }

