    public void thongke1(){
        String sql = "select c.masp, sum(soluong), s.tensp from Chitiethoadon as c  left join sanpham as s on c.masp = s.masp group by s.tensp, c.masp order by sum(soluong) desc";
                ConnectDB cn = new ConnectDB();
                cn.getCn();
                ResultSet rset = cn.executeQuery(sql);
                String masp = "";
                String soluong = "";
                String tensp = "";
                try {
                    while(rset.next()){
                    
                    masp = rset.getString(1);
                    soluong = rset.getString(2);
                    tensp = rset.getString(3);
                    Vector v = new Vector();
                    
                    v.add(masp);
                    v.add(tensp);
                    v.add(soluong);
                    data.add(v);
                    tbsp.setModel(new DefaultTableModel(data, title));
                    }
                       
                } catch (SQLException ex) {
                }
                cn.close();
    }
  
